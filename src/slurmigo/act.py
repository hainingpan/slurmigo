from __future__ import annotations

import csv
import io
import logging
import os
import re
import stat
import subprocess

logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Params file parsing
# ---------------------------------------------------------------------------


def parse_params_file(path: str) -> tuple[list[dict[str, str]], str]:
    """Parse a parameter file and return (list_of_param_dicts, format_type).

    Auto-detects format:
      - Lines with ``=`` → key=value format
      - Lines with ``,`` but no ``=`` → CSV (first line is header)
      - Otherwise → raw text, key="SLURMIGO_LINE"
    """
    with open(path, "r") as f:
        raw_lines = f.readlines()

    # Strip and filter empty / comment lines
    lines = []
    for line in raw_lines:
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        lines.append(stripped)

    if not lines:
        return [], "keyvalue"

    # Auto-detect format from first data line
    first = lines[0]
    if "=" in first:
        format_type = "keyvalue"
    elif "," in first and "=" not in first:
        format_type = "csv"
    else:
        format_type = "raw"

    params: list[dict[str, str]] = []

    if format_type == "keyvalue":
        for line in lines:
            try:
                tokens = line.split()
                entry: dict[str, str] = {}
                for token in tokens:
                    key, value = token.split("=", 1)
                    entry[key] = value
                params.append(entry)
            except ValueError:
                logger.warning("Skipping unparseable key=value line: %s", line)

    elif format_type == "csv":
        header = lines[0]
        data_lines = lines[1:]
        reader = csv.DictReader(
            io.StringIO("\n".join([header] + data_lines)),
        )
        for row in reader:
            try:
                params.append(dict(row))
            except Exception:
                logger.warning("Skipping unparseable CSV row: %s", row)

    else:
        # Raw text — each line becomes SLURMIGO_LINE
        for line in lines:
            params.append({"SLURMIGO_LINE": line})
        format_type = "raw"

    return params, format_type


def count_tasks(path: str) -> int:
    """Count valid (non-empty, non-comment) lines in a params file.

    For CSV files: subtracts 1 for the header row.
    """
    with open(path, "r") as f:
        raw_lines = f.readlines()

    lines = []
    for line in raw_lines:
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        lines.append(stripped)

    if not lines:
        return 0

    # Detect format to know if first line is a header
    first = lines[0]
    if "," in first and "=" not in first:
        # CSV — header doesn't count
        return len(lines) - 1

    return len(lines)


