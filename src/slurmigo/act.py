# Wrapper generation
# ---------------------------------------------------------------------------


def generate_wrapper(
    task_id: int,
    params: dict[str, str],
    user_script: str,
    state_dir: str,
) -> str:
    """Generate a bash wrapper that exports params and sources the user script.

    Creates ``{state_dir}/wrappers/task_{task_id}.sh``, makes it executable,
    and returns its absolute path.
    """
    wrappers_dir = os.path.join(state_dir, "wrappers")
    os.makedirs(wrappers_dir, exist_ok=True)

    wrapper_path = os.path.join(wrappers_dir, f"task_{task_id}.sh")
    abs_user_script = os.path.abspath(user_script)

    lines = [
        "#!/bin/bash",
        "# Generated by slurmigo — do not edit",
        f"# Task ID: {task_id}",
    ]

    for key, value in params.items():
        lines.append(f'export {key}="{value}"')

    lines.append(f"export SLURMIGO_TASK_ID={task_id}")
    lines.append(f'source "{abs_user_script}"')
    lines.append("")  # trailing newline

    with open(wrapper_path, "w") as f:
        f.write("\n".join(lines))

    os.chmod(
        wrapper_path,
        stat.S_IRWXU | stat.S_IRGRP | stat.S_IXGRP | stat.S_IROTH | stat.S_IXOTH,
    )  # 755

    return os.path.abspath(wrapper_path)


def build_sbatch_args(
    directives: dict[str, str],
    overrides: dict[str, str] | None = None,
) -> list[str]:
    """Convert a directive dict to a list of sbatch CLI args.

    Each key-value becomes ``--key=value``.  If *overrides* is provided,
    those key-value pairs replace the originals.
    """
    merged = dict(directives)
    if overrides:
        merged.update(overrides)

    args: list[str] = []
    for key, value in merged.items():
        if value == "":
            # Boolean flag like --requeue (no value)
            args.append(f"--{key}")
        else:
            args.append(f"--{key}={value}")

    return args


# ---------------------------------------------------------------------------
# Submission
# ---------------------------------------------------------------------------


def submit_job(wrapper_path: str, sbatch_args: list[str]) -> str | None:
    """Submit a job via sbatch and return the job ID string, or None on failure."""
    cmd_list = ["sbatch"] + sbatch_args + [wrapper_path]

    try:
        result = subprocess.run(
            cmd_list,
            capture_output=True,
            text=True,
            check=True,
        )
        match = re.search(r"Submitted batch job (\d+)", result.stdout)
        if match:
            return match.group(1)
        logger.error("sbatch output did not contain job ID: %s", result.stdout)
        return None
    except subprocess.CalledProcessError as exc:
        logger.error("sbatch failed (rc=%d): %s", exc.returncode, exc.stderr)
        return None
    except FileNotFoundError:
        logger.error("sbatch command not found")
        return None


# ---------------------------------------------------------------------------
# Cleanup
# ---------------------------------------------------------------------------


def cleanup_wrappers(state_dir: str, keep_last: int = 0) -> None:
    """Remove wrapper scripts from ``{state_dir}/wrappers/``.

    If *keep_last* > 0, keep the most recently modified *keep_last* files
    and delete the rest.  If *keep_last* == 0, delete all.
    """
    wrappers_dir = os.path.join(state_dir, "wrappers")
    if not os.path.isdir(wrappers_dir):
        return

    files = [
        os.path.join(wrappers_dir, f)
        for f in os.listdir(wrappers_dir)
        if os.path.isfile(os.path.join(wrappers_dir, f))
    ]

    if keep_last <= 0:
        for fp in files:
            os.remove(fp)
        return

    # Sort by modification time, newest first
    files.sort(key=lambda p: os.path.getmtime(p), reverse=True)

    for fp in files[keep_last:]:
        os.remove(fp)
